/* Imports ////////////////////////////////////////////////////////////////////////////////////////////////////////// */

import { readonly, ref } from 'vue';

/* State //////////////////////////////////////////////////////////////////////////////////////////////////////////// */

const routerLoading = ref(false);

export const setRouterLoading = (value: boolean) => {
  routerLoading.value = value;
};

/* Compose ////////////////////////////////////////////////////////////////////////////////////////////////////////// */

export const useRouterLoading = () => {
  return readonly(routerLoading);
};
